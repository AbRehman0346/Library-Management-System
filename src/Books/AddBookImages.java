    private String setImages(JLabel label){
        String location = "";
        FileDialog imgFront = new FileDialog(this, "Select Book Picture", FileDialog.LOAD);
        imgFront.setVisible(true);
        try{
            location = imgFront.getDirectory() + imgFront.getFile();
            label.setIcon(MainForm.setImage(location, label.getWidth(), label.getHeight()));
        }
        catch(NullPointerException e){
            label.setIcon(null); 
            label.setText("                Optional");
            location = "";
        }
        return location;
    }
