    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        EncryptPassword ep = new EncryptPassword();
        String email = tfEmail.getText();
        String password = String.valueOf(pfPassword.getPassword());
        
        try {
            connection();
            
            {//Deleting Old Settings so that new settings can be saved.
                PreparedStatement ps3 = con.prepareStatement("DELETE FROM email_settings where ID = 1");
                ps3.execute();
            }
            
            ps = con.prepareStatement("INSERT INTO email_settings (autoEmail1, `autoemail2`, autoemail3, autoemail4, ID)\n" +
                "VALUES (?, ?, ?, ?, ?)");
            ps.setString(1, ((togleBtn1.isSelected()) ? "ON" : "OFF"));
            ps.setString(2, ((togleBtn2.isSelected()) ? "ON" : "OFF"));
            ps.setString(3, ((togleBtn3.isSelected()) ? "ON" : "OFF"));
            ps.setString(4, ((togleBtn4.isSelected()) ? "ON" : "OFF"));
            ps.setString(5, "1");
            ps.execute();
            
            if (!password.equals("")){
                String subject = "LMS Default Email Address.";
                String content = "<h2>This Email Address has been Set as Default Email Address Of LMS (Library Management System)</h2>";
                new SendEmail().sendEmail(email, password, email, subject, content);
                
                {//Deleting Old table so that new settings can be saved.
                    deleteEmailAddressFromSettings();
                }
                
                PreparedStatement ps2 = con.prepareStatement("INSERT INTO email_settings (Email, `password`, ID)\n" +
                    "VALUES (?, ?, ?)");
                ps2.setString(1, ep.Encrypt(email));
                ps2.setString(2, ep.Encrypt(String.valueOf(password)));
                ps2.setString(3, "2");  
                ps2.execute();
            }
            else if (email.equals("") && password.equals("")){
                deleteEmailAddressFromSettings();
            }

            new AutomaticEmail().sendEmailAtReturnDate();
            new AutomaticEmail().sendEmailAfterReturnDate();
            JOptionPane.showMessageDialog(this, "Settings Saved", "Emails", JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
            
        } catch (MailConnectException e){
            JOptionPane.showMessageDialog(this, "Failed!\nNo Internet Connection.\nCan't verify new email", "Email", JOptionPane.ERROR_MESSAGE);
        }
        catch (AuthenticationFailedException e){
            String msg = "Failed!\nWrong Email Address or Password. \nIf you get this Error Continuously then go to your Email Address Settings and Turn on the Less secure App"
                    + " Settings then try Again.";
            JOptionPane.showMessageDialog(this, msg, "Email", JOptionPane.ERROR_MESSAGE);
        }
        catch (MessagingException ex) {
            JOptionPane.showMessageDialog(this, ex, "Email", JOptionPane.ERROR_MESSAGE);
            //Logger.getLogger(EmailSettings.class.getName()).log(Level.SEVERE, null, ex);
        }
        catch (SQLException | ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(this, "Failed to Save Settings", "Emails", JOptionPane.INFORMATION_MESSAGE);
            //Logger.getLogger(EmailSettings.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

