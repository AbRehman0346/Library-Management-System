    private void setSettings(){
        EncryptPassword ep = new EncryptPassword();
        try {
            connection();
            ps = con.prepareStatement("SELECT ID, EMAIL, PASSWORD, AUTOEMAIL1 as a1, AUTOEMAIL2 as a2, AUTOEMAIL3 as a3, AUTOEMAIL4 as a4 FROM Email_Settings "
                    + "WHERE ID = 1 OR ID = 2");
            ResultSet rs = ps.executeQuery();
            while (rs.next()){
                if (rs.getString("ID").equals("1")){
                    if (rs.getString("a1").equals("ON")){togleBtn1.setSelected(true); togleBtn1.setText("ON");}
                    if (rs.getString("a2").equals("ON")){togleBtn2.setSelected(true); togleBtn2.setText("ON");}
                    if (rs.getString("a3").equals("ON")){togleBtn3.setSelected(true); togleBtn3.setText("ON");}
                    if (rs.getString("a4").equals("ON")){togleBtn4.setSelected(true); togleBtn4.setText("ON");}
                }else if (rs.getString("ID").equals("2")){
                    emailAddress = ep.Encrypt(rs.getString("Email"));
                    tfEmail.setText(emailAddress);
                }
            }
        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(EmailSettings.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
