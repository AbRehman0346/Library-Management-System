    private void lblForgotPasswordMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblForgotPasswordMouseClicked
        try {
            MainForm.connection();
            ps = MainForm.con.prepareStatement("SELECT email, question, answer FROM password WHERE id =1");
            ResultSet rs = ps.executeQuery();
            if (rs.next()){
                EncryptPassword ep = new EncryptPassword();
                String email = null;
                String question = null;
                String answer = null;
                try{email = ep.Encrypt(rs.getString("email"));}catch(NullPointerException e){}
                try{question = ep.Encrypt(rs.getString("question"));}catch(NullPointerException e){}
                try{answer = ep.Encrypt(rs.getString("answer"));}catch(NullPointerException e){}
                if (!(email == null)){
                    Recover_Password_By_Email r = new Recover_Password_By_Email();
                    r.Email_Receiver(email, answer);
                    r.setVisible(true);
                }
                else if (!(question == null)){
                    Recover_Password_By_Question r = new Recover_Password_By_Question();
                    r.Question_Answer_Receiver(question, answer);
                    r.setVisible(true);
                }
                else {
                    JOptionPane.showMessageDialog(rootPane, "No any Recovery action is Set!\n Now Contact Developer", 
                            "Password Recovery", JOptionPane.ERROR_MESSAGE);
                }
            }else JOptionPane.showMessageDialog(rootPane, "Sorry any Error Occoured!", "Error", JOptionPane.ERROR_MESSAGE);
        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_lblForgotPasswordMouseClicked

