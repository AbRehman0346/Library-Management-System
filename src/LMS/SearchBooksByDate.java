    private void setBooksBookData(String query){
        try {
            tm.setRowCount(0);
            connection();
            int i = 0;
            ps = con.prepareStatement(query);
            ResultSet rs = ps.executeQuery();
            while (rs.next()){
                Vector v = new Vector();
                v.add(rs.getString("id"));
                v.add(rs.getString("TITLE"));
                v.add(rs.getString("Author"));
                v.add(rs.getString("Subject"));
                v.add(rs.getString("Publisher"));
                v.add(new All_Information().change(rs.getString("Edition")));
                v.add(new All_Information().change(rs.getString("Language")));
                v.add(new All_Information().changePrice(rs.getString("Price") + " " + rs.getString("Currency")));
                v.add(rs.getString("Place"));
                v.add(new All_Information().change(rs.getString("Pages")));
                v.add(rs.getString("Keyword"));
                v.add(rs.getString("ISBN"));
                v.add(rs.getString("status"));
                v.add(rs.getString("Date"));
                tm.addRow(v);
                i++;
            }
            lblBookCount.setText((i != 0) ? ("Books: " + i) : "No Record Found!");
            if (i==0){JOptionPane.showMessageDialog(this, "No Record Found.", "Search By Date", JOptionPane.INFORMATION_MESSAGE);}
        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(All_Information.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
