    public Custom_Emails(String[] emails) {
        initComponents();
        
        StringBuilder email = new StringBuilder();
        for (String i : emails){
            try{i.equals("");}catch(NullPointerException e){continue;}
            email.append(i).append(", ");
        }
        try{
            email.deleteCharAt(email.length()-2);
            tfRecepient.setText(email.toString());
        }catch(NullPointerException | StringIndexOutOfBoundsException e){}
    }
    
