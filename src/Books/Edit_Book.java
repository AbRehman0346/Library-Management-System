    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        try{
            InputStream frontImage=null, backImage=null;
            String frontImageQuery = "", backImageQuery = "";
            boolean isFrontImageChoosen = false, isBackImageChoosen = false;
            int numberOfPictures = 13;
            try{
                frontImage = new FileInputStream(imgBookFrontImageLocation);
                frontImageQuery = ", frontImage=?";
                isFrontImageChoosen = true;
            }catch(FileNotFoundException e){}
            
            try{
                backImage = new FileInputStream(imgBookBackImageLocation);
                backImageQuery = ", backImage=?";
                isBackImageChoosen = true;
            }catch(FileNotFoundException e){}
            
            MainForm.connection();
            ps = MainForm.con.prepareStatement("UPDATE book SET title=?, author=?, subject=?, publisher=?, edition=?, language=?, price=?, currency=?,"
                    + " place=?, pages=?, keyword=?, ISBN=?, `group`=? "+frontImageQuery+" "+backImageQuery+" WHERE id = " + lblId.getText());
            ps.setString(1, txtTitle.getText());  if (txtTitle.getText().equals("")) throw new NewException("Book Title Must be Provided.");
            ps.setString(2, txtAuthor.getText()); if (txtAuthor.getText().equals("")) throw new NewException("Book Author Must be Provided.");
            ps.setString(3, txtSubject.getText());
            ps.setString(4, txtPublisher.getText());
            ps.setString(5, spnrEdition.getValue().toString());
            try{ps.setString(6, cbLanguage.getSelectedItem().toString());}catch(NullPointerException | SQLException e){ps.setString(6, "");}
            ps.setString(7, (txtPrice.getText().equals("") ? "0" : txtPrice.getText()));
            try{ps.setString(8, cbCurrency.getSelectedItem().toString());}catch(NullPointerException ex){ps.setString(8, "Rs");}
            ps.setString(9, txtPlace.getText());
            ps.setString(10, spnrPages.getValue().toString());
            ps.setString(11, txtKeyword.getText());
            ps.setString(12, txtIsbn.getText());
            try{ps.setString(13, cbGroup.getSelectedItem().toString());}catch (NullPointerException | SQLException e){ps.setString(13, "");}
            
            if (isFrontImageChoosen)
                ps.setBlob(++numberOfPictures, frontImage);
            
            if (isBackImageChoosen)
                ps.setBlob(++numberOfPictures, backImage);
            
            if (!ps.execute()){
                if (checkBoxCantIssueStateChanged){
                    ps = con.prepareStatement("SELECT `STATUS` FROM BOOK WHERE ID = '"+lblId.getText()+"'");
                    ResultSet rs = ps.executeQuery();
                    if (rs.next()){
                        if ((rs.getString("status").equals("Available") || rs.getString("status").startsWith("Reserved")) && 
                                checkBoxCantIssue.isSelected()){
                            ps = con.prepareStatement("UPDATE BOOK SET `STATUS` = 'Private' WHERE id = '"+lblId.getText()+"'");
                            ps.execute();
                        }
                        else if (rs.getString("status").equals("Private") && !checkBoxCantIssue.isSelected()){
                            ps = con.prepareStatement("UPDATE BOOK SET `STATUS` = 'Available' WHERE id = '"+lblId.getText()+"'");
                            ps.execute();
                        }
                        else
                            JOptionPane.showMessageDialog(this, "Can't Change the Status of Book", "Can't Issue Settings", JOptionPane.ERROR_MESSAGE);
                    }
                }
                JOptionPane.showMessageDialog(null, "Book \""+txtTitle.getText()+"\" has been Updated Sucessfully.", "Update Book", JOptionPane.INFORMATION_MESSAGE);
                this.dispose();
            }
            else
            JOptionPane.showMessageDialog(null, txtTitle.getText()+"could not be Updated.", "Update Book", JOptionPane.ERROR_MESSAGE);
        }
        catch(SQLException | ClassNotFoundException | NewException e)
        {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            //Logger.getLogger(Add_Book.class.getName()).log(Level.SEVERE, null, e);
        }
    }//GEN-LAST:event_btnAddActionPerformed

