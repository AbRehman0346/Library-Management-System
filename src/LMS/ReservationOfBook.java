    private void settingData(){
        try {
            MainForm.connection();
            tm.setRowCount(0);
            ps = MainForm.con.prepareStatement("SELECT b.id as bid, b.title, b.author, R.STUDENT_id as sid, sd.first_name, sd.last_name, sd.group, b.status FROM LMS.RESERVE_BOOK R\n" +
                    "LEFT JOIN lms.student_data sd ON sd.id = student_id\n" +
                    "LEFT JOIN lms.book b ON b.id = book_id");
            ResultSet rs = ps.executeQuery();
            while (rs.next()){
                Vector v = new Vector();
                v.add(rs.getString("sid"));
                v.add(rs.getString("First_Name") + " " + rs.getString("Last_Name"));
                v.add(rs.getString("group"));
                v.add(rs.getString("bid"));
                v.add(rs.getString("title"));
                v.add(rs.getString("author"));
                v.add(statusChanger(rs.getString("status")));
                tm.addRow(v);
            }
            
        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(ReservationOfBook.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    private String statusChanger(String status){
        return status.substring(0, 8).equals("Reserved") ? "Available" : status;
    }

