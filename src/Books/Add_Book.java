    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        try{
            MainForm.connection();
            InputStream frontImage = null;
            InputStream backImage = null;
            try{
                frontImage = new FileInputStream(imgBookFrontImage);
                backImage = new FileInputStream(imgBookBackImage);
            }
            catch(FileNotFoundException e){}
            ps = MainForm.con.prepareStatement("INSERT INTO lms.book(id, title, author, `subject`, publisher, edition, `language`, price, currency, place, pages,\n" +
                "keyword, ISBN, `status`, `group`, `DATE`, `frontImage`, `backImage`)\n" +
                                                " VALUES(?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)");
            ps.setString(1, lblId.getText());
            ps.setString(2, txtTitle.getText());  if (txtTitle.getText().equals("")) throw new NewException("Book Title Must be Provided.");
            ps.setString(3, txtAuthor.getText()); if (txtAuthor.getText().equals("")) throw new NewException("Book Author Must be Provided.");
            ps.setString(4, txtSubject.getText());
            ps.setString(5, txtPublisher.getText());
            ps.setString(6, spnrEdition.getValue().toString());
            try{ps.setString(7, cbLanguage.getSelectedItem().toString());}catch(NullPointerException e){ps.setString(7, "");}
            ps.setString(8, (txtPrice.getText().equals("") ? "0" : txtPrice.getText()));
            ps.setString(9, cbCurrency.getSelectedItem().toString());
            ps.setString(10, txtPlace.getText());
            ps.setString(11, spnrPages.getValue().toString());
            ps.setString(12, txtKeyword.getText());
            ps.setString(13, txtIsbn.getText());
            ps.setString(14, checkBoxCantIssue.isSelected() ? "Private" : "Available");
            ps.setString(15, cbGroup.getSelectedItem().toString());
            ps.setString(16, lblDate.getText());
            ps.setBlob(17, frontImage);
            ps.setBlob(18, backImage);
            if (!ps.execute()){
                JOptionPane.showMessageDialog(null, "Book \""+txtTitle.getText()+"\" has been Added Sucessfully.", "Add Book", JOptionPane.INFORMATION_MESSAGE);
                imgBookFrontImage = "";
                imgBookBackImage = "";
                this.dispose();
            }
            else
                JOptionPane.showMessageDialog(null, txtTitle.getText()+"could not be Added.", "Add Book", JOptionPane.ERROR_MESSAGE);
        }
        catch(SQLException | ClassNotFoundException | NewException e)
        {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            //Logger.getLogger(Add_Book.class.getName()).log(Level.SEVERE, null, e);
        }
    }//GEN-LAST:event_btnAddActionPerformed

