    private void btnBooksExportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBooksExportActionPerformed
        try {
            DateFormat df = new SimpleDateFormat("yyyy-MM-dd");
            FileDialog file = new FileDialog(this, "Select Path", FileDialog.SAVE);
            file.setFile("Books Issued Today");
            file.setVisible(true);
            String location = file.getDirectory() + file.getFile() + " " + df.format(new Date()) + ".xls";
            if (location.substring(0, 4).equals("null"))
                throw new NewException("");
            File f = new File(location);
            if (new All_Information().exportTable(tbl, f)){
                JOptionPane.showMessageDialog(this, "Sucessfully Created Your File.");
            }else
            JOptionPane.showConfirmDialog(this, "Sorry! File Could Not Be Created.");
        } catch(NewException e){}
        catch (IOException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
            //Logger.getLogger(BooksReturnedToday.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnBooksExportActionPerformed

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        String query = "";
        switch(cbDate.getSelectedIndex()){
            case 0 -> {query = "SELECT * FROM BOOK WHERE `DATE` > '"+ tfYear.getText() + "-" + tfMonth.getText() + "-" + tfDay.getText() +"'";}
            case 1 -> {query = "SELECT * FROM BOOK WHERE `DATE` < '"+ tfYear.getText() + "-" + tfMonth.getText() + "-" + tfDay.getText() +"'";}
            case 2 -> {query = "SELECT * FROM BOOK WHERE `DATE` = '"+ tfYear.getText() + "-" + tfMonth.getText() + "-" + tfDay.getText() +"'";}
            default -> {query = "SELECT * FROM BOOK WHERE `DATE` BETWEEN '"+ tfYear.getText() + "-" + tfMonth.getText() + "-" + tfDay.getText() +
                    "' AND '"+cbDate.getSelectedItem()+"'";}
        }
        setBooksBookData(query);
    }//GEN-LAST:event_btnSearchActionPerformed

