    private void timer() {
        new Thread(()->{
            btnResendCode.setEnabled(false);
            for (int i=60; i>=0; i--){
                try{Thread.sleep(1000);}catch(InterruptedException e){System.out.println(e.getMessage());}
                lbltimer.setText(i + " sec");
            }
            btnResendCode.setEnabled(true);
        }).start();
    }
    
