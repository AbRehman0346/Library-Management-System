    private void tfPasswordFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_tfPasswordFocusLost
        String password = String.valueOf(tfPassword.getPassword());
        if(password.equals(""))
            tfPassword.setEchoChar((char)0);
        tfPassword.setText(focusLostLogic(password, "Enter Password"));
    }//GEN-LAST:event_tfPasswordFocusLost

    private void loginLogic(){
        String password = "";
        {   //Getting password from database and saving in password variable...for future use...
            try {
                MainForm.connection();
                ps = MainForm.con.prepareStatement("SELECT password FROM password WHERE id = 1");
                ResultSet rs = ps.executeQuery();
                if (rs.next()){
                    Abdul_Rehmancom.company.EncryptPassword ep = new Abdul_Rehmancom.company.EncryptPassword();
                    password = ep.Encrypt(rs.getString("password"));
                }else{
                    JOptionPane.showMessageDialog(rootPane, "Password not found in Database");
                }
            } catch (SQLException | ClassNotFoundException ex) {
                Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
        if (String.valueOf(tfPassword.getPassword()).equals(password)){
            new Deciding_First_Page_To_Open().chooseFile();
            this.dispose();
        }
        else{
            lblWrongPassword.setText("Wrong Password!");
            count++;
            if (count == 3)
                lblForgotPassword.setVisible(true);
        }
    }
    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        checkBoxShowPassword.setSelected(false);
        tfPassword.setEchoChar('*');
        loginLogic();    
    }//GEN-LAST:event_btnLoginActionPerformed

