    private void togleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_togleActionPerformed
        String t = togle.getText();
        if (t.equals("Active"))
            togle.setText("Deactive");
        else if (t.equals("Deactive"))
            togle.setText("Active");
    }//GEN-LAST:event_togleActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            connection();
            ps = con.prepareStatement("UPDATE STUDENT_DATA\n" +
                    "SET `status` = '"+togle.getText()+"',\n" +
                    "`description` = '"+ta.getText()+"'\n" +
                    "WHERE ID = '"+lblId.getText()+"'");
            ps.execute();
            JOptionPane.showMessageDialog(this, "Changes Saved", "Activation", JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(Activate_Student_Account.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

