    private void btnRemovePasswordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemovePasswordActionPerformed
        try {
            MainForm.connection();
            ps = MainForm.con.prepareStatement("SELECT password, status FROM password WHERE id = 1");
            ResultSet rs = ps.executeQuery();
            if(!rs.next())
                throw new MySecondException();
            else if (rs.getString("status").equals("Current")){
                PasswordConfromDialog password = new PasswordConfromDialog();
                EncryptPassword ep = new EncryptPassword();
                password.Password_Receiver_From_Remove_Password(ep.Encrypt(rs.getString("password")));
                password.setVisible(true);
            }
            else
                throw new MySecondException();
        } catch (SQLException | ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        } catch (MySecondException ex) {
            JOptionPane.showMessageDialog(rootPane, "Password is not Set", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnRemovePasswordActionPerformed

