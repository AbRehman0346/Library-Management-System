    private void setcbBookGroups(){
        try {
            connection();
            comboBoxGroup.addItem("None");
            ps = con.prepareStatement("SELECT DISTINCT `group` FROM LMS.BOOK\n" +
                    "WHERE `group` is not null AND `group` != 'none'");
            ResultSet rs = ps.executeQuery();
            while (rs.next()){
                comboBoxGroup.addItem(rs.getString("GROUP"));
            }
            comboBoxGroup.addItem("Custom");
        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(Add_Book.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
