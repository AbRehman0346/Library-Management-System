    private void recoverPassword(){
        if (RecoverPassword.matchCode(tfInput.getText())){
            Set_Password sp = new Set_Password();
            sp.lblHeading.setText("Change Password");
            sp.btnSet.setText("Change");
            sp.setVisible(true);
            this.dispose();
        }
        else
            lblWrongCode.setText("Wrong Code");
    }
