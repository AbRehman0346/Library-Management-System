    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        try{
            InputStream frontImage=null, backImage=null;
            String frontImageQuery = "", backImageQuery = "";
            try{
                frontImage = new FileInputStream(imgBookFrontImageLocation);
                frontImageQuery = ", frontImage = " + frontImage;
            }catch(FileNotFoundException e){}
            
            try{
                backImage = new FileInputStream(imgBookBackImageLocation);
                backImageQuery = ", backImage = " + backImage;
            }catch(FileNotFoundException e){}
            
            StringBuilder sb = new StringBuilder();
            
            if (cbSubject.isSelected()){sb.append("subject = '").append(txtSubject.getText()).append("'");}
            if (cbPublisher.isSelected()){sb.append(", publisher = '").append(txtPublisher.getText()).append("'");}
            if (cbEdition.isSelected()){sb.append(", edition = '").append(spnrEdition.getValue()).append("'");}
            try{if (cbLanguage.isSelected()){sb.append(", language = '").append(comboBoxLanguage.getSelectedItem()).append("'");}}catch(NullPointerException e){}
            if (cbPrice.isSelected()){sb.append(", price = '").append(txtPrice.getText()).append("', Currency = '").append(comboBoxCurrency.getSelectedItem()).append("'");}
            if (cbPlace.isSelected()){sb.append(", place = '").append(txtPlace.getText()).append("'");}
            if (cbPages.isSelected()){sb.append(", pages = '").append(spnrPages.getValue()).append("'");}
            if (cbKeyword.isSelected()){sb.append(", keyword = '").append(txtKeyword.getText()).append("'");}
            if (cbCategory.isSelected()){sb.append(", `group` = '").append(comboBoxGroup.getSelectedItem()).append("'");}
            
            if (!(cbSubject.isSelected())){sb.deleteCharAt(0);}
            
            String query = "UPDATE book SET " + sb + frontImageQuery + " " + backImageQuery + booksIds;
            
            MainForm.connection();
            
            ps = MainForm.con.prepareStatement(query);
            
            if (!ps.execute()){
                JOptionPane.showMessageDialog(null, txtTitle.getText()+" has been Updated Sucessfully.", "Update Book", JOptionPane.INFORMATION_MESSAGE);
                this.dispose();
            }
            else
            JOptionPane.showMessageDialog(null, txtTitle.getText()+"could not be Updated.", "Update Book", JOptionPane.ERROR_MESSAGE);
        }
        catch(SQLException | ClassNotFoundException e)
        {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            //Logger.getLogger(Add_Book.class.getName()).log(Level.SEVERE, null, e);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

