    public Activate_Student_Account(String studentId) {
        initComponents();
        {
            lblId.setText(studentId);
            try {
                connection();
                ps = con.prepareStatement("SELECT `status`, `description` FROM STUDENT_DATA WHERE id = '"+studentId+"'");
                ResultSet rs = ps.executeQuery();
                if (rs.next()){
                    togle.setText(rs.getString("Status"));
                    ta.setText(rs.getString("description"));
                }
            } catch (SQLException | ClassNotFoundException ex) {
                Logger.getLogger(Activate_Student_Account.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }

