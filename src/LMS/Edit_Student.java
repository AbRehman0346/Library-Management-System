    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        InputStream is = null;
        String imageQuery = ", `image` = DEFAULT ";
        boolean isImageChoosen = false;
        String email = txtEmail.getText();
        try{
            is = new FileInputStream(imgLocation);
            imageQuery = ",`image` = ? ";
            isImageChoosen = true;
        }catch (NullPointerException | FileNotFoundException e){}
        //java.util.logging.Logger.getLogger(Edit_Student.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
        String message = "This Email is Already Present\nDo you want to continue";
        try {
            if (new Add_Student(true).isEmailPresentInDatabase(email) && (!this.email.equals(email)))
                if (JOptionPane.showConfirmDialog(this, message, "Duplicate Email", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE) == JOptionPane.NO_OPTION)
                    throw new NewException("Email Already Present.");
            connection();
            ps = con.prepareStatement("UPDATE `student_Data`\n" +
                                "SET `First_Name` = ?,\n" +
                                "`Last_Name` = ?,\n" +
                                "`Father_Name` = ?,\n" +
                                "`Phone` = ?,\n" +
                                "`Email` = ?,\n" +
                                "`group` = ?,\n" +
                                "`Sex` = ?,\n" +
                                "`Address` = ?\n" + imageQuery +
                                "WHERE ID = '"+lblID.getText()+"'");
            ps.setString(1, txtFirstName.getText());
            ps.setString(2, txtLastName.getText());
            ps.setString(3, tfFatherName.getText());
            ps.setString(4, txtPhone.getText());
            ps.setString(5, email);
            ps.setString(6, cbGroup.getSelectedItem().toString());
            ps.setString(7, cbSex.getSelectedItem().toString());
            ps.setString(8, taAddress.getText());
            if (isImageChoosen)
                ps.setBlob(9, is);
                
            if (!(ps.execute())) {
                this.dispose();
            }
        } catch (SQLException e) {
            java.util.logging.Logger.getLogger(Edit_Student.class.getName()).log(java.util.logging.Level.SEVERE, null, e);
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        } catch (NullPointerException e) {
            JOptionPane.showMessageDialog(rootPane, "Please Provide all the information.");
        } catch (ClassNotFoundException ex) {
            java.util.logging.Logger.getLogger(Edit_Student.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
        } catch (NewException ex) {}
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void txtPhoneKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPhoneKeyReleased
        if (txtPhone.getText().length() == 4 && txtPhone.getText().charAt(0) == '0' && evt.getKeyCode() != KeyEvent.VK_BACK_SPACE)
            txtPhone.setText(txtPhone.getText() + " ");
        
        if ((txtPhone.getText().length() == 3 || txtPhone.getText().length() == 7) && txtPhone.getText().charAt(0) == '+' && evt.getKeyCode() != KeyEvent.VK_BACK_SPACE)
            txtPhone.setText(txtPhone.getText() + " ");
    }//GEN-LAST:event_txtPhoneKeyReleased

