    public boolean deleteReservation(String studentId, String bookId) throws SQLException, ClassNotFoundException{
        connection();
        ps = con.prepareStatement("DELETE FROM LMS.RESERVE_BOOK WHERE student_id = '"+studentId+
                        "' AND book_id = '"+bookId+"'");
        return !ps.execute();
    }
    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        try{
            if (tblReservationData.getSelectionModel().isSelectionEmpty())
                throw new NewException("Nothing is Selected to Delete.");
            for (int selectedRows : tblReservationData.getSelectedRows()){
                connection();
                ps = con.prepareStatement("SELECT book_id FROM lms.issue_book WHERE book_id = '"+tm.getValueAt(selectedRows, 3)+"'");
                ResultSet rs = ps.executeQuery();
                if (!rs.next()){
                    ps = con.prepareStatement("UPDATE LMS.BOOK SET `STATUS` = 'Available' WHERE ID = '"+tm.getValueAt(selectedRows, 3)+"'");
                    ps.execute();
                }
                
                if (deleteReservation(tm.getValueAt(selectedRows, NORMAL).toString(), tm.getValueAt(selectedRows, 3).toString()) ){
                    JOptionPane.showMessageDialog(this, "Reservation is Deleted.", "Delete Reservation", JOptionPane.INFORMATION_MESSAGE);
                    settingData();
                }
                    
            }
        } catch (SQLException | ClassNotFoundException | NewException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            //Logger.getLogger(ReservationOfBook.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnIssueBookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIssueBookActionPerformed
        if (!tblReservationData.getSelectionModel().isSelectionEmpty())
            new IssueBook(tm.getValueAt(tblReservationData.getSelectedRow(), NORMAL).toString(), 
                tm.getValueAt(tblReservationData.getSelectedRow(), 3).toString()).setVisible(true);
        else
            JOptionPane.showMessageDialog(this, "First Select Any Record From Table", "Issue Book", JOptionPane.ERROR_MESSAGE);
    }//GEN-LAST:event_btnIssueBookActionPerformed

