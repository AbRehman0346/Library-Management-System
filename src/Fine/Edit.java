    private void updateFine() {
        try {
            DateFormat df = new SimpleDateFormat("yyyy-MM-dd");
            
            
            if (tfFine.getText().equals("")) {
                throw new NewException("Please Specify the Fine Amount.");
            }

            String purpose = comboBoxPurpose.getSelectedItem().toString();
            String fine = tfFine.getText();
            String description = taDescription.getText();
            String fineId = lblFineId.getText();
            String lastDate = df.format(dateChooserLastDate.getDate());

            connection();
            ps = con.prepareStatement("UPDATE FINE\n"
                    + "SET PURPOSE = '" + purpose + "',\n"
                    + "FINE = '" + fine + "',\n"
                    + "last_date = '" + lastDate + "',\n"
                    + "`DESCRIPTION` = '" + description + "'\n"
                    + "WHERE ID = '" + fineId + "'");

            if (!ps.execute()) {
                JOptionPane.showMessageDialog(this, "Update Sucessful.");

                {//Updating the CurrentFine Records to the recently Changed Records.
                    CurrentFines f = CurrentFines.object;
                    f.search();
                }

                this.dispose();
            }
        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(Edit.class.getName()).log(Level.SEVERE, null, ex);
        } catch (NewException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Input Error", JOptionPane.ERROR_MESSAGE);
        }
    }

