    private void btnOkActionPerformedLogic(){
        if (answer.toUpperCase().equals(tfAnswer.getText().toUpperCase())){
            Set_Password sp = new Set_Password();
            sp.lblHeading.setText("Change Password");
            sp.btnSet.setText("Change");
            sp.setVisible(true);
            this.dispose();
        }
        else 
            lblWrongAnswer.setText("Wrong Answer");
    }
    private void btnOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOkActionPerformed
        btnOkActionPerformedLogic();
    }//GEN-LAST:event_btnOkActionPerformed

    private void tfAnswerKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tfAnswerKeyReleased
        if (evt.getKeyCode() == java.awt.event.KeyEvent.VK_ENTER)
            btnOkActionPerformedLogic();
        else
            lblWrongAnswer.setText(null);
    }//GEN-LAST:event_tfAnswerKeyReleased

