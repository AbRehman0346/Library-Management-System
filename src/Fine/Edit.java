    public Edit(String fineId) {
        initComponents();

        comboBoxItems();

        String memberId = "";
        String bookId = "";

        try {
            connection();

            {//Searching for fine Information (Fine ID, Fine Amount, Purpose, Description)
                ps = con.prepareStatement("SELECT id, STUDENT_ID, BOOK_ID, last_date, FINE, PURPOSE, `DESCRIPTION` FROM FINE "
                        + "WHERE ID = '" + fineId + "'");
                ResultSet rs = ps.executeQuery();
                if (rs.next()) {
                    lblFineId.setText(rs.getString("id"));
                    memberId = rs.getString("student_id");
                    bookId = rs.getString("book_id");
                    tfFine.setText(rs.getString("fine"));
                    comboBoxPurpose.setSelectedItem(rs.getString("purpose"));
                    taDescription.setText(rs.getString("description"));
                    dateChooserLastDate.setDate(rs.getDate("last_date"));
                }
            }

            {//Searching Previous Fines of this Member.
                ps = con.prepareStatement("SELECT SUM(FINE) AS TFINE FROM FINE \n"
                        + "WHERE student_id = " + memberId + "");
                ResultSet rs = ps.executeQuery();
                if (rs.next()) {
                    lblPreviousFine.setText(rs.getString("TFine"));
                }
            }
            {//Searching Member and filling necessary Information...
                ps = con.prepareStatement("SELECT first_name AS fn, Last_Name AS ln FROM student_data\n"
                        + "WHERE ID = '" + memberId + "'");
                ResultSet rs = ps.executeQuery();
                if (rs.next()) {
                    memberName = rs.getString("fn") + " " + rs.getString("ln");
                }

                lblMemberId.setText(memberId);
                lblName.setText(memberName);

            }

            {//Searching for Book Data...
                ps = con.prepareStatement("SELECT TITLE AS T, PRICE AS P FROM book\n"
                        + "WHERE ID = " + bookId + "");
                ResultSet rs = ps.executeQuery();
                if (rs.next()) {
                    lblBookPrice.setText(rs.getString("p"));
                    lblBookId.setText(bookId);
                    lblTitle.setText(rs.getString("t"));
                }
            }
        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(Edit.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void comboBoxItems() {
        try {
            connection();
            ps = con.prepareStatement("SELECT DISTINCT(PURPOSE) FROM FINE");
            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
                comboBoxPurpose.addItem(rs.getString("purpose"));
            }
            comboBoxPurpose.addItem("Custom");
        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(Edit.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

