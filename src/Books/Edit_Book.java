    public Edit_Book(String bookId){
        initComponents();
        setcbBookGroups();
        try {
            connection();
            ps = con.prepareStatement("SELECT * FROM book WHERE id = '" + bookId + "'");
            ResultSet rs = ps.executeQuery();
            if (rs.next()){
                lblId.setText(bookId);
                txtTitle.setText(rs.getString("title"));
                txtAuthor.setText(rs.getString("author"));
                txtSubject.setText(rs.getString("subject"));
                txtPublisher.setText(rs.getString("Publisher"));
                try{spnrEdition.setValue(Integer.parseInt(rs.getString("edition")));}catch(NumberFormatException e){}
                
                txtPrice.setText(rs.getString("Price"));
                cbCurrency.setSelectedItem(rs.getString("Currency"));
                txtPlace.setText(rs.getString("Place"));
                spnrPages.setValue(Integer.parseInt(rs.getString("pages")));
                txtKeyword.setText(rs.getString("keyword"));
                txtIsbn.setText(rs.getString("isbn"));
                
                if (rs.getString("status").equals("Private"))
                    checkBoxCantIssue.setSelected(true);
                
                cbGroup.setSelectedItem(rs.getString("group"));
                lblDate.setText(rs.getString("date"));
                
                try{lblBookFrontImage.setIcon(MainForm.setImage(rs.getBytes("frontImage"), lblBookFrontImage.getWidth(), lblBookFrontImage.getHeight()));}
                catch(NullPointerException e){}
                
                try{lblBookBackImage.setIcon(MainForm.setImage(rs.getBytes("backImage"), lblBookBackImage.getWidth(), lblBookBackImage.getHeight()));}
                catch(NullPointerException e){}
                
                {//Setting Languages....
                    new Add_Book().setLanguages(cbLanguage);
                    cbLanguage.setSelectedItem(rs.getString("Language"));
                }
                
            }
        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(Edit_Book.class.getName()).log(Level.SEVERE, null, ex);
        }    
    }

    private String setImages(JLabel label) throws NullPointerException{
        String location = "";
        FileDialog imgFront = new FileDialog(this, "Select Book Picture", FileDialog.LOAD);
        imgFront.setVisible(true);
        
        location = imgFront.getDirectory() + imgFront.getFile();
        label.setIcon(MainForm.setImage(location, label.getWidth(), label.getHeight()));
        
        return location;
    }
    
    private void setcbBookGroups(){
        try {
            connection();
            cbGroup.addItem("None");
            ps = con.prepareStatement("SELECT DISTINCT `group` FROM BOOK\n" +
                    "WHERE `group` is not null AND `group` != 'none'");
            ResultSet rs = ps.executeQuery();
            while (rs.next()){
                cbGroup.addItem(rs.getString("GROUP"));
            }
            cbGroup.addItem("Custom");
        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(Add_Book.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
