    private void setcbBookGroups(){
        try {
            connection();
            cbGroup.addItem("None");
            ps = con.prepareStatement("SELECT DISTINCT `group` FROM LMS.BOOK\n" +
                    "WHERE `group` is not null AND `group` != 'none'");
            ResultSet rs = ps.executeQuery();
            while (rs.next()){
                cbGroup.addItem(rs.getString("GROUP"));
            }
            cbGroup.addItem("Custom");
        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(Add_Book.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    void setLanguages(JComboBox cb) {
        try {
            connection();
            ps = con.prepareStatement("SELECT DISTINCT(`language`) as L FROM BOOK");
            ResultSet rs = ps.executeQuery();
            while (rs.next())
                cb.addItem(rs.getString("L"));
            cb.addItem("Custom");
        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(Add_Book.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    private String setImages(JLabel label){
        String location = "";
        FileDialog imgFront = new FileDialog(this, "Select Book Picture", FileDialog.LOAD);
        imgFront.setVisible(true);
        try{
            location = imgFront.getDirectory() + imgFront.getFile();
            label.setIcon(MainForm.setImage(location, label.getWidth(), label.getHeight()));
        }
        catch(NullPointerException e){
            label.setIcon(null); 
            label.setText("                   Optional");
            location = "";
        }
        return location;
    }
    
    private String autoGenerateId() {
        String id = "ID NOT FOUND";
        try{
            MainForm.connection();
            ps = MainForm.con.prepareStatement("SELECT id  FROM lms.book\n" +
                                                "ORDER BY id DESC\n" +
                                                "LIMIT 1");
            ResultSet rs = ps.executeQuery();
            if (rs.next()){
                id = String.format("%04d", (1+Long.parseLong(rs.getString("id"))));
            }else{
                id = "0001";
            }
        }catch(SQLException | ClassNotFoundException e)
        {Logger.getLogger(Add_Book.class.getName()).log(Level.SEVERE, null, e);}
        return id;
    }
