    private void openSetPasswordDialog(){
        try {
            MainForm.connection();
            ps = MainForm.con.prepareStatement("SELECT password, status FROM password WHERE id = 1");
            ResultSet rs = ps.executeQuery();
            if(!rs.next())
                new Set_Password().setVisible(true);
            else if(rs.getString("status").equals("Current")){
                PasswordConfromDialog password = new PasswordConfromDialog();
                EncryptPassword ep = new EncryptPassword();
                password.Password_Receiver_For_Change(ep.Encrypt(rs.getString("password")));
                password.setVisible(true);
            }
            else 
                new Set_Password().setVisible(true);
        } catch (SQLException | ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        } 
    }
