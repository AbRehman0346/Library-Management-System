    public Edit_Student(String id){
        initComponents();
        {
            cbGroup.addItem("None");
            addCategory();
            cbGroup.addItem("Custom");
        }
        
        taAddress.setLineWrap(true);
        taAddress.setWrapStyleWord(true);
        try {
            connection();
            ps = con.prepareStatement("SELECT * FROM student_Data\n" +
                    "WHERE ID = \""+id+"\"");
            ResultSet rs = ps.executeQuery();
            if (rs.next()){
                lblID.setText(id);
                txtFirstName.setText(rs.getString("first_name"));
                txtLastName.setText(rs.getString("last_name"));
                tfFatherName.setText(rs.getString("Father_name"));
                txtPhone.setText(rs.getString("phone"));
                cbSex.setSelectedItem(rs.getString("sex"));
                taAddress.setText(rs.getString("address"));
                cbGroup.setSelectedItem(rs.getString("group"));
                this.email = rs.getString("Email");
                txtEmail.setText(email);
                try{lblPicture.setIcon(MainForm.setImage(rs.getBytes("image"), lblPicture.getWidth(), lblPicture.getHeight()));}
                catch(NullPointerException e){lblPicture.setText("     Picture Not Found");}
            }
        } catch (SQLException | ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            //Logger.getLogger(Edit_Student.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    public final void addCategory(){
        try {
            connection();
            ps = con.prepareStatement("SELECT DISTINCT `group` FROM LMS.student_data \n" +
                "WHERE `group` is not null AND `group` != 'none'");
            ResultSet rs = ps.executeQuery();
            while (rs.next()){
                cbGroup.addItem(rs.getString("group"));
            }
        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(Add_Student.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
